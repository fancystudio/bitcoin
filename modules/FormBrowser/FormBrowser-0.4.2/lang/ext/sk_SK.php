<li> Read the note above about making sure your form has a Database Disposition!</li>
<li> Use unique ordering of fields in the Browser configuration. Using position 1 for two different fields, for example, will clobber one of the two.</li>
<li> Last resport is to email me or catch me on IRC and we can go from there.</li>
