<h3>How Do I Create a Browser?</h3>
<p>In the CMS Admin Menu, you should get a new menu item called FormBrowser under Content. Click on this. On the page
that gets shown, there are options (at the bottom of the list of Browsers, if any) to Add a New Browser or Modify
Configuration.</p>
<p>Once you&#039;ve created a browser, you need to select a FormBuilder form for which it displays results. If this FormBuilder form doesn&#039;t use the Database Disposition, you&#039;re not going to see any results, so make sure you pick a form that makes sense!</p>
<p>Once you&#039;ve selected a form, you can re-order the field display in four places: the list view and the detail or full view, both on the user side and the admin side. You can also set a bunch of options, such as the number of rows displayed in list views, whether or not editing/adding/deleting is permitted on the user side, and so on. You can edit the templates for the display of the list view and full detail view on the user side.</p>
<p>When re-ordering fields, make sure that you think of the numbers as &quot;slots&quot; rather than &quot;weights&quot; -- in other words, don&#039;t use the same order number for two different fields, or it won&#039;t work the way you think it should.</p>
<h3>Administering a Browser</h3>
<p>By clicking on the key next to a browser in the admin list, you can go in and add/edit/delete/approve/disapprove records. If the form was created with User Approval, you won&#039;t see it in the admin unless the user has validated.</p>

<h3>Adding a Browser to a Page</h3>
<p>In the main FormBrowser admin page, you can see an example of the tag used to display each form. It looks
something like {cms_module module=&#039;FormBrowser&#039; browser=&#039;sample_browser&#039;}</p>
<p>By copying this tag into the content of a page, or into a template, will cause that browser to be displayed.
In theory, you can have multiple browsers on a page if you really want to, although this would be a sure sign of mental impairment and total disregard for database abuse. Be careful when pasting the tag
into a page&#039;s content if you use a WYSIWYG editor such as TinyMCE or HTMLArea. These editors may stealthily
change the quote marks (&quot;) into HTML entities (&amp;amp;quot;), and the browser will not show up. Try using
single quotes (&#039;) or editing the HTML directly.</p>
<h3>Sorting</h3>
<p>Experimental code to allow sorting has been introduced. Because of the crufty architecture, it&#039;s very memory-intensive. A future release will fix this (but require a rewrite).</p>
<p>In your tag, you can specify a default sort field and direction, e.g., {cms_module module=&#039;FormBrowser&#039; browser=&#039;sample_browser&#039; fbrp_sort_field=&#039;Name&#039; fbrp_sort_dir=&#039;a&#039;} where sort_dir can be &#039;a&#039; for ascending or &#039;d&#039; for descending.
The sort is currently a case-independent string sort, so it may not do the right thing with dates or numbers (yet).</p>
<p>If you do NOT want columns to be sortable, edit your template. Look for the section that is like:<br />
<pre>{section name=namelist start=0 loop=$count}
		<th>{$sortingnames[$smarty.section.namelist.index]}</th>
{/section}
</pre>
<p>Use $sortingnames if you want the columns to be sortable, use $names if you don&#039;t.</p>
<h3>Providing Search to Users</h3>
<p>Alberto Benati added code to enable searching of fields! If you add fbrp_searchfield=&quot;name&quot; or fbrp_searchfield=&quot;name1,name2&quot; to your FormBrowser tag (and you&#039;re using the most recent BrowserListDefault template), you will get search input box(es) on your browser list page.</p>

<h3>Troubleshooting</h3>
