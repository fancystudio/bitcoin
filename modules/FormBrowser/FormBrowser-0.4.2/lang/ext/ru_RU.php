<h3>Как создать обозреватель?</h3>
<p>В меню административного интерфейса CMS должен появиться новый пункт, "Обозреватель форм", под пунктом "Контент". Щёлкните его. На странице,
которая откроется, есть кнопка для создания нового обозреватель. Она находится под списком существующих обозревателей -- если таковые есть. На этой странице можно также изменить
Настройки.</p>
<p>При создании обозревателя требуется выбрать форму Конструктора форм, данные которой будут отображаться в обозревателе. Если эта форма не содержит команду сохранения данных в формате Обозревателя форм, Вы не увидите её данных в обозревателе -- обозреватель для такой формы бесполезен!</p>
<p>Выбрав форму, Вы можете задать порядок отображения полей для четырёх случаев: для отображения списка записей, для полного (детального) отображения отдельной записи -- и то и другое отдельно для пользователей и для администратора. При этом можно задать ряд настроек, как, например, количество строк при представлении списком, разрешить ли изменение/добавление/удаление пользоателям, и так далее. Вы можете редактировать шаблоны отображения списка и отдельной записи на стороне пользователя.</p>
<p>При определении порядка полей числа означают "позиции", а не "веса" -- другими словами, не используйте одно и то же число для двух разных полей, иначе результат может не соответствовать Вашим ожиданиям.</p>
<h3>Администрирование обозревателя</h3>
<p>Нажав на соответствющий значок напротив названия обозревателя, можно добавлять/изменять/удалять/одобрять и отменять одобрение записей. Если форма требует подтверждения пользователя, её данные не будут показаны, пока пользователь не пройдёт проверку.</p>

<h3>Добавление обозревателя на страницу</h3>
<p>На стартовой странице администрирования Обозревателя форм для каждой формы приведён пример тега, отображающего форму. Он имеет
следующий вид: {cms_module module=\'FormBrowser\' browser=\'sample_browser\'}</p>
<p>Скопируйте этот тег и вставьте на страницу (или в шаблон) -- и в соответствующем месте страницы (или шаблона) появится обозреватель.
Теоретически, можно разместить несколько обозревателей на одной странице, если очень хочется, хотя это будет явным признаком психического расстройства и абсолютного безразличия к неправильному использованию базы данных. Будьте внимательны при вставке тега
в контент страницы, если Вы используете WYSIWYG редактор (наподобие TinyMCE или HTMLArea). Эти редакторы могут незаметно
заменить кавычки (") сущностями HTML (&amp;quot;) -- в результате обозреватель не появится. Попробуйте использовать
одинарные кавычки (\') или отредактируйте HTML вручную.</p>
<h3>Сортировка</h3>
<p>Внутренняя реализация сортировки использует индексные поля, задаваемые в Конструкторе форм. Когда вы добавляете на форму
команду сохранения данных в формате Обозревателя форм, Вы выбираете, по каким поля можно выполнить сортировку. Выбранные поля волшебным образом
оказываются сортируемыми здесь, в Обозревателе форм.</p>
<p>Чтобы отключить сортировку, отредактируйте шаблон. Найдите следующий участок кода:<br />
<pre>{section name=namelist start=0 loop=\\$count}
		<th>{\\$sortingnames[\\$smarty.section.namelist.index]}</th>
{/section}
</pre>
<p>Используйте $sortingnames, если Вы хотите разрешить сортировку, используйте $names, если не хотите.</p>
<h3>Фильтрация результатов поиска</h3>
<p>Если Вы добавите filter_field=\\"name\\" и filter_value=\\"value\\" в тег Обозревателя форм, будут отображаться только те записи, у которых поле \\"name\\" имеет значение \\"value\\". Для фильтрации можно использовать имена полей от \\"index1\\" до
\\"index5\\" -- они соответствуют полям, по которым выполняется сортировка.</p>

